package com.epam.news.model.entity;


public class Tag {

    private int tag;
    private String name;

    public Tag() {
    }

    public int getTag() {
        return tag;
    }

    public void setTag(int tag) {
        this.tag = tag;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
